class Prodotto():
    def __init__(self, nome, descrizione, immagine, valori_nutrizionali, lista_ingredienti,
                 categoria, sottocategoria, prezzo, personalizzazione):
        super(Prodotto, self).__init__()
        self.nome=nome
        self.descrizione=descrizione
        self.immagine=immagine
        self.valori_nutrizionali=valori_nutrizionali
        self.lista_ingredienti=lista_ingredienti
        self.categoria=categoria
        self.sottocategoria=sottocategoria
        self.prezzo=prezzo
        self.personalizzazione=personalizzazione












